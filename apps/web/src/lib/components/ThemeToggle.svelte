<script lang="ts">
	import { theme } from '$lib/theme.svelte.js';

	const modes = ['light', 'system', 'dark'] as const;

	function cycle() {
		const idx = modes.indexOf(theme.choice);
		theme.set(modes[(idx + 1) % modes.length]);
	}
</script>

<button
	onclick={cycle}
	class="relative flex h-7 w-7 items-center justify-center rounded-lg
	       text-text-dim transition hover:text-text hover:bg-accent/5
	       focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-accent/40"
	title="Theme: {theme.choice}"
	aria-label="Toggle theme (currently {theme.choice})"
>
	{#if theme.choice === 'light'}
		<!-- Sun -->
		<svg class="h-3.5 w-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="1.5">
			<path stroke-linecap="round" stroke-linejoin="round" d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z" />
		</svg>
	{:else if theme.choice === 'system'}
		<!-- Monitor -->
		<svg class="h-3.5 w-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="1.5">
			<path stroke-linecap="round" stroke-linejoin="round" d="M9 17.25v1.007a3 3 0 01-.879 2.122L7.5 21h9l-.621-.621A3 3 0 0115 18.257V17.25m6-12V15a2.25 2.25 0 01-2.25 2.25H5.25A2.25 2.25 0 013 15V5.25A2.25 2.25 0 015.25 3h13.5A2.25 2.25 0 0121 5.25z" />
		</svg>
	{:else}
		<!-- Moon -->
		<svg class="h-3.5 w-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="1.5">
			<path stroke-linecap="round" stroke-linejoin="round" d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z" />
		</svg>
	{/if}
</button>